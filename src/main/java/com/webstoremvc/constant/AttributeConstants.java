package com.webstoremvc.constant;

/**
 * Constants for attribute values
 */
public enum AttributeConstants {
	/**
	 * Name of the attribute that contains info about login
	 */
	ATTRIBUTE_LOGIN, 
	
	/**
	 * Represent boolean true for login attribute
	 */
	LOGIN_TRUE, 
	
	/**
	 * Represent boolean false for login attribute
	 */
	LOGIN_FALSE,
	
	/**
	 * Name of the attribute that contains info about shopping cart
	 */
	ATTRIBUTE_SHOPPING_CART,
	
	/**
	 * Name of the attribute that contains info about register operation
	 */
	ATTRIBUTE_REGISTER,
	
	/**
	 * Indicates successful register
	 */
	ATTRIBUTE_REGISTER_SUCCESS,
	
	/**
	 * Indicates unsuccessful register
	 */
	ATTRIBUTE_REGISTER_FAILURE;
}
